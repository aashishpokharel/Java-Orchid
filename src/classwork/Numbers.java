package classwork;


public class Numbers {
		public static void main(String[] args)
		{
			// float
			double value1 = 10.0, value2 = 9.87f;
			double output = value1 + value2;
			System.out.println("double sum\n"+ value1+"+"+value2+ " = "+ output);
			// Integer operation
			int i1 = 10, i2 = 9;
			int op = i1 + i2;
			System.out.println("Integer sum\n"+ i1+"+"+i2+ " = "+ op);
		}    
}


